package ch.bbw.jh;

public class Calculator {
    public int summe(int summe1, int summe2){
        return summe1+summe2;
    }
    public int subtraktion(int value1, int value2){
        return value1 - value2;
    }
    public int division(int value1, int value2){
        return value1/value2;
    }
}
